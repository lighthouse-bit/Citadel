// Helper functions
export const getFeaturedArtworks = () => {
  return sampleArtworks.filter(artwork => artwork.featured);
};

export const getArtworkById = (id) => {
  return sampleArtworks.find(artwork => artwork.id === String(id) || artwork.id === `sample-${id}`);
};

export const getArtworkBySlug = (slug) => {
  return sampleArtworks.find(artwork => artwork.slug === slug);
};

export const getRelatedArtworks = (currentId, limit = 3) => {
  return sampleArtworks
    .filter(artwork => artwork.id !== currentId && artwork.status === 'AVAILABLE')
    .slice(0, limit);
};